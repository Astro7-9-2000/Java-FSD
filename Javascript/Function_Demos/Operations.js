// Summation
function sum(a, b)
{
    let total = 0;
    total = a + b;
    return total;
}
let sum_result = sum(40, 50);
console.log(sum_result);


// Difference
function difference(x , y)
{
    let difference = 0;
    difference = x - y;
    return difference;
}

let difference_result = difference(90, 20);
console.log(difference_result);

// Multiplication
function Product(c, d) {
  let Product = 0;
  Product = c * d;
  return Product;
}

let product_result = Product(5, 9);
console.log(product_result);

// Division
function Quotient(u, v)
{
    let Quotient = 0;
    Quotient = v / u;
    return Quotient;
}

let Quotient_Result = Quotient(8, 4);
console.log(Quotient_Result);